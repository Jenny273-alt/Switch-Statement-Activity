let day = prompt("Enter a day of the week:");

// Convert to lowercase for case-insensitive comparison
day = day.toLowerCase();

// Use a switch statement to assign tasks
switch (day) {
  case "monday":
    console.log("Your task for Monday is: Class.");
    break;
  case "tuesday":
    console.log("Your task for Tuesday is: Study session.");
    break;
  case "wednesday":
    console.log("Your task for Wednesday is: Library time.");
    break;
  case "thursday":
    console.log("Your task for Thursday is: Group work.");
    break;
  case "friday":
    console.log("Your task for Friday is: Social activity.");
    break;
  case "saturday":
    console.log("Your task for Saturday is: Relax and recharge.");
    break;
  case "sunday":
    console.log("Your task for Sunday is: Plan for the week ahead.");
    break;
  default:
    console.log("Invalid day of the week. Please enter a valid day.");
}

